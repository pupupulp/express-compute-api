exports.add = (operands) => {
	return operands.a + operands.b;
};

exports.subtract = (operands) => {
	return operands.a - operands.b;
};

exports.multiply = (operands) => {
	return operands.a * operands.b;
};

exports.divide = (operands) => {
	return operands.a / operands.b;
};

exports.mod = (operands) => {
	return operands.a % operands.b;
};